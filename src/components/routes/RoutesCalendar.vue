<script lang="ts">
/**
 * RoutesCalendar Component
 *
 * @description * Use this component to render a calendar for logging routes.
 * This component is used on `RoutesPage` in `RouteTabs` component.
 *
 * @components
 * - `CalendarNavigation`: Component to render navigation buttons.
 * - `CalendarItemDisplay`: Component to render calendar items.
 * - `RouteCalendarPanel`: Component to render dialog panel.
 *
 * @example
 * <routes-calendar />
 *
 * @see [Figma Design](https://www.figma.com/design/L8dVREySVXxh3X12TcFDdR/Do-pr%C3%A1ce-na-kole?node-id=4858-104006&t=kwUDasKoJ1urpPut-1)
 */

// libraries
import { colors } from 'quasar';
import { QCalendarMonth, today } from '@quasar/quasar-ui-qcalendar';
import { defineComponent, computed, ref, watch } from 'vue';
import { i18n } from '../../boot/i18n';

// components
import CalendarItemDisplay from './CalendarItemDisplay.vue';
import CalendarNavigation from './CalendarNavigation.vue';
import RouteCalendarPanel from './RouteCalendarPanel.vue';

// types
import type { Timestamp } from '@quasar/quasar-ui-qcalendar';
import type {
  RouteCalendarActive,
  RouteCalendarDay,
  RouteItem,
  TransportDirection,
} from '../types/Route';

// fixtures
import routesListCalendarFixture from '../../../test/cypress/fixtures/routeListCalendar.json';

export default defineComponent({
  name: 'RoutesCalendar',
  components: {
    CalendarItemDisplay,
    CalendarNavigation,
    RouteCalendarPanel,
  },
  setup() {
    const calendar = ref<typeof QCalendarMonth | null>(null);
    const selectedDate = ref<string>(today());
    const locale = computed((): string => {
      return i18n.global.locale;
    });

    // Define calendar CSS vars for calendar theme
    const { getPaletteColor } = colors;
    const theme = {
      '--calendar-active-date-color': getPaletteColor('primary'),
      '--calendar-current-color': getPaletteColor('primary'),
      '--calendar-border-current': `${getPaletteColor('primary')} 2px solid`,
    };

    // Compute month name and year for title
    const monthNameAndYear = computed((): string => {
      if (!selectedDate.value) {
        return '';
      }
      const date = new Date(selectedDate.value);
      const month = date.toLocaleString(locale.value, { month: 'long' });
      const year = date.toLocaleString(locale.value, { year: 'numeric' });
      return `${month} ${year}`;
    });

    // Calendar naviation functions
    function onToday(): void {
      calendar.value?.moveToToday();
    }
    function onPrev(): void {
      calendar.value?.prev();
    }
    function onNext(): void {
      calendar.value?.next();
    }

    // Get data
    const routes: RouteCalendarDay[] =
      routesListCalendarFixture as RouteCalendarDay[];

    /**
     * Computed property of the routes map.
     * Contains an array of days - each with two routes:
     * - to work
     * - from work
     */
    const routesMap = computed((): Record<string, RouteCalendarDay> => {
      const routesObject = {} as Record<string, RouteCalendarDay>;
      if (routes.length > 0) {
        routes.forEach((route) => {
          routesObject[route.date] = route;
        });
      }
      return routesObject;
    });

    // Active state
    const activeRoutes = ref<RouteCalendarActive[]>([]);

    const activeRoutesComputed = computed((): RouteItem[] => {
      return activeRoutes.value.map(
        (activeRoute: RouteCalendarActive): RouteItem => {
          if (activeRoute.timestamp && activeRoute.direction) {
            /**
             * You are selecting from logged days.
             * You need to enable routes that are not logged by creating a fresh route to write into.
             */
            const day: RouteCalendarDay =
              routesMap.value[activeRoute.timestamp.date];
            if (day && activeRoute.direction === 'toWork') {
              return { ...day.toWork };
            }
            if (day && activeRoute.direction === 'fromWork') {
              return { ...day.fromWork };
            } else {
              return {
                id: '',
                date: activeRoute.timestamp.date,
                direction: activeRoute.direction,
                transport: 'bike',
                distance: 0,
                inputType: 'input-number',
              };
            }
          } else {
            return {
              id: '',
              date: '',
              direction: 'toWork',
              transport: 'bike',
              distance: 0,
              inputType: 'input-number',
            };
          }
        },
      );
    });

    /**
     * Determines if route item is active.
     * It checks timestamp and direction against a stored routes.
     * @param {Object} { timestamp: Timestamp; direction: TransportDirection }
     * @return {boolean}
     */
    function isActive({
      timestamp,
      direction,
    }: {
      timestamp: Timestamp;
      direction: TransportDirection;
    }): boolean {
      if (
        !timestamp ||
        !direction ||
        !activeRoutes.value ||
        !activeRoutes.value.length
      ) {
        return false;
      }
      return getActiveIndex({ timestamp, direction }) > -1;
    }

    /**
     * Finds the index of the active route based on timestamp and direction.
     * @param {Object} payload - Object containing the timestamp and
     * the direction of the route item.
     * - `timestamp` (Timestamp): The timestamp of the route item.
     * - `direction` (TransportDirection): The direction of the route item.
     * @return {number} The index of the active route or -1 if route not found.
     */
    function getActiveIndex({
      timestamp,
      direction,
    }: {
      timestamp: Timestamp;
      direction: TransportDirection;
    }): number {
      if (
        !timestamp ||
        !direction ||
        !activeRoutes.value ||
        !activeRoutes.value.length
      ) {
        return -1;
      }
      return activeRoutes.value.findIndex((activeRoute) => {
        return (
          activeRoute.timestamp?.date === timestamp?.date &&
          activeRoute.direction === direction
        );
      });
    }

    /**
     * Handles click on route item within a day frame.
     * It triggers active state on that day.
     * It controls content of the route-logging dialog panel.
     * @param {Object} { timestamp: Timestamp; direction: TransportDirection }
     * @return {void}
     */
    function onClickItem({
      timestamp,
      direction,
    }: {
      timestamp: Timestamp;
      direction: TransportDirection;
    }): void {
      if (isActive({ timestamp, direction })) {
        activeRoutes.value.splice(getActiveIndex({ timestamp, direction }), 1);
      } else {
        activeRoutes.value.push({ timestamp, direction });
      }
    }

    /**
     * Triggered when user saves active routes.
     * Clears active selection
     */
    function onSave(): void {
      activeRoutes.value = [];
    }

    const isOpenPanel = ref<boolean>(false);
    // control dialog open state based on selected routes count
    watch(
      (): number => activeRoutes.value.length,
      (length): void => {
        if (length === 0) {
          isOpenPanel.value = false;
        } else {
          isOpenPanel.value = true;
        }
      },
    );

    return {
      activeRoutes,
      activeRoutesComputed,
      calendar,
      isOpenPanel,
      locale,
      monthNameAndYear,
      routesMap,
      selectedDate,
      theme,
      isActive,
      onClickItem,
      onNext,
      onPrev,
      onSave,
      onToday,
    };
  },
});
</script>

<template>
  <div data-cy="routes-calendar">
    <!-- Top bar -->
    <div class="row q-pb-sm q-my-lg items-center gap-8">
      <!-- Title -->
      <div
        class="col-12 col-sm-auto text-h5 text-capitalize text-weight-bold"
        data-cy="calendar-title"
      >
        {{ monthNameAndYear }}
      </div>
      <!-- Navigation buttons -->
      <calendar-navigation
        @next="onNext"
        @prev="onPrev"
        @today="onToday"
        class="col-12 col-sm"
      />
    </div>
    <!-- Calendar -->
    <div class="row justify-center q-mt-lg">
      <q-calendar-month
        ref="calendar"
        v-model="selectedDate"
        animated
        bordered
        hoverable
        no-active-date
        use-navigation
        short-weekday-label
        :locale="locale"
        :show-month-label="false"
        :weekdays="[1, 2, 3, 4, 5, 6, 0]"
        weekday-align="center"
        :style="theme"
        date-align="right"
        date-type="rounded"
        :day-min-height="100"
      >
        <template #day="{ scope: { timestamp, outside } }">
          <div v-if="!timestamp.future" class="q-my-sm" data-cy="calendar-day">
            <!-- Route to work -->
            <calendar-item-display
              :active="isActive({ timestamp, direction: 'toWork' })"
              :disabled="outside"
              direction="toWork"
              :day="routesMap[timestamp.date]"
              :timestamp="timestamp"
              @item-click="onClickItem"
              data-cy="calendar-item-display-to-work"
            />
            <!-- Route from work -->
            <calendar-item-display
              :active="isActive({ timestamp, direction: 'fromWork' })"
              :disabled="outside"
              direction="fromWork"
              :day="routesMap[timestamp.date]"
              :timestamp="timestamp"
              @item-click="onClickItem"
              data-cy="calendar-item-display-from-work"
            />
          </div>
        </template>
      </q-calendar-month>
    </div>
    <route-calendar-panel
      v-model="isOpenPanel"
      :routes="activeRoutesComputed"
      @save="onSave"
    />
  </div>
</template>
